export function useCompanyIcons() {
  const [companyIcons, setCompanyIcons] = useState<Record<string, string>>(() => {
    try {
      const saved = localStorage.getItem('companyIcons');
      return saved ? JSON.parse(saved) : {};
    } catch (error) {
      console.error('Error loading company icons:', error);
      return {};
    }
  });

  useEffect(() => {
    localStorage.setItem('companyIcons', JSON.stringify(companyIcons));
  }, [companyIcons]);

  const getCompanyIcon = (companyId: string): string => {
    return companyIcons[companyId] || 'building';
  };

  const setCompanyIcon = (companyId: string, iconId: string) => {
    setCompanyIcons(prev => ({
      ...prev,
      [companyId]: iconId
    }));
  };

  const getIconById = (iconId: string): ReactNode => {
    const icon = AVAILABLE_ICONS.find(i => i.id === iconId);
    return icon ? icon.icon : AVAILABLE_ICONS[0].icon;
  };

  return {
    availableIcons: AVAILABLE_ICONS,
    companyIcons,
    getCompanyIcon,
    setCompanyIcon,
    getIconById
  };
}
